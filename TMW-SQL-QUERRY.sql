SELECT 
TLDTL.RATE,
TLDTL.RATE_UNITS,
TLORDER.DISTANCE,
TLORDER.ORIGIN,
TLORDER.DESTINATION,
TLORDER.COMMODITY,
TLORDER.BILL_DATE,
TLORDER.DELIVERY_TRIP,
TLORDER.TRACE_NO,
TLORDER.BILL_NUMBER,
TLDTL.RATE,
TLORDER.ORIGIN,
TLORDER.ORIGNAME,
TLORDER.ORIGADDR1,
TLORDER.ORIGCITY,
TLORDER.ORIGPROV,
TLORDER.ORIGPC,
TLORDER.DESTINATION,
TLORDER.DESTNAME,
TLORDER.DESTADDR1,
TLORDER.DESTCITY,
TLORDER.DESTPROV,
TLORDER.DESTPC,
TLORDER.DISTANCE,
TLORDER.DISTANCE_UNITS,
TLORDER.TOTAL_CHARGES,
TLORDER.COMMODITY
FROM TLORDER, TLDTL
WHERE TLDTL.ORDER_ID = TLORDER.DETAIL_LINE_ID
AND TLORDER.BILL_DATE > '2013-01-10 0:00:00' 
AND TLORDER.BILL_DATE < '2013-05-31 0:00:00'
AND TLORDER.ORIGIN = '010001'
ORDER BY TLORDER.ORIGIN,
TLORDER.ORIGNAME,
TLORDER.ORIGADDR1,
TLORDER.ORIGCITY,
TLORDER.ORIGPROV,
TLORDER.ORIGPC,
TLORDER.DESTINATION,
TLORDER.DESTNAME,
TLORDER.DESTADDR1,
TLORDER.DESTCITY,
TLORDER.DESTPROV,
TLORDER.DESTPC,
TLORDER.COMMODITY,
TLORDER.DISTANCE,
TLORDER.DISTANCE_UNITS,
TLDTL.RATE,
TLORDER.BILL_DATE;